     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required',
            'age' => 'required'
        ]);
        $random = new Random();
        $random->name = $request->name;
        $random->age = $request->age;
        return response()->json([
            'success' => true,
            'data' => $random->toArray()
        ]);
    }

